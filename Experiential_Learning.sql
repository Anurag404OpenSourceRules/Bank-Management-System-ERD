-- CREATING ALL THE TABLES/RELATIONS
CREATE TABLE Bank(
code number(5),
name varchar2(20),
address varchar2(50), 
PRIMARY KEY(code));


CREATE TABLE Employee(
employee_id number(10),
date_of_join date,
Qualification varchar2(40),
Age number(3),
First_Name varchar2(20),
Last_Name varchar2(20),
EBranch_id number(5),   -- Foreign Key for Branch Table
PRIMARY KEY(employee_id));

-- MULTIVALUED ATTRIBUTE TABLE
CREATE TABLE Employee_Phone(
Phone number(10));


CREATE TABLE Branch(
branch_id number(5),
Name varchar2(20),
Address varchar2(50),
PRIMARY KEY(branch_id));


CREATE TABLE Account(
account_number number(5),
Account_Name varchar2(20),
Account_Balance number(15),
PRIMARY KEY(account_number));


CREATE TABLE Loan(
loan_number number(5),
Loan_Type varchar2(20),
Loan_Amount varchar2(15),
PRIMARY KEY(loan_number));


CREATE TABLE Customer(
customer_id number(10),
First_Name varchar2(20),
Last_Name varchar2(20),
Address varchar2(50),
PRIMARY KEY(customer_id));

-- MULTIVALUED ATTRIBUTE TABLE
CREATE TABLE Customer_Phone(
customer_id number(10),
customer_phone number(10));

-- WEAK ENTITY TABLE
CREATE TABLE Related_dependent(
Name varchar2(20),
Sex varchar2(8),
Date_of_Birth date,
Relationship varchar2(15));



-- The Tables Schema
describe Bank;
describe Employee;
describe Employee_Phone;
describe Branch;
describe Account;
describe Loan;
describe Holds;
describe Avails;
describe Customer;
describe Customer_Phone;
describe Related_Dependent;



-- Adding FOREIGN KEYS
    -- Employees work at Branch
ALTER TABLE Employee
ADD CONSTRAINT works_at FOREIGN KEY(EBranch_id)
REFERENCES Branch(branch_id) ON DELETE SET NULL;


    -- Employees can have multiple phone numbers(multivalued attribute)
ALTER TABLE Employee_Phone
ADD Employee_id number(10);

    -- Adding FOREIGN KEY to reference Employee
ALTER TABLE Employee_Phone
ADD CONSTRAINT employee_has FOREIGN KEY(Employee_id)
REFERENCES Employee(Employee_id) on delete set null;

    -- Creating a composite PRIMARY KEY for the Employee_Phone table
ALTER TABLE Employee_Phone
ADD CONSTRAINT pair_primary_key1 PRIMARY KEY(Employee_id, Phone);


    -- A Bank can have multiple Branches
ALTER TABLE Branch
ADD Bank_Code number(5);

ALTER TABLE Branch
ADD CONSTRAINT bank_has_branches FOREIGN KEY(bank_code)
REFERENCES Bank(Code) ON DELETE SET NULL;


    -- A Branch can Maintain Multiple Accounts
ALTER TABLE Account
ADD Branch_Account_id number(5);

ALTER TABLE Account
ADD CONSTRAINT branch_maintains FOREIGN KEY(Branch_Account_id)
REFERENCES Branch(Branch_id);


    -- A Branch can Offer Multiple Loans
ALTER TABLE Loan
ADD Branch_Loan_id number(5);

ALTER TABLE Loan
ADD CONSTRAINT branch_offers FOREIGN KEY(Branch_Loan_id)
REFERENCES Branch(Branch_id);


    -- Relationship Table Holds
CREATE TABLE Holds(
Account_Number number(5),
Customer_id number(10));

ALTER TABLE Holds
ADD PRIMARY KEY(Account_Number, Customer_id);

        -- Adding FOREIGN KEYS
ALTER TABLE Holds
ADD CONSTRAINT customer_holds1 FOREIGN KEY(Account_Number)
REFERENCES Account(Account_number) ON DELETE SET NULL;

ALTER TABLE Holds
ADD CONSTRAINT customer_holds2 FOREIGN KEY(Customer_id)
REFERENCES Customer(Customer_id) ON DELETE SET NULL;


    -- Relationship Table Avails
CREATE TABLE Avails(
Loan_Number number(5),
Customer_id number(10));

ALTER TABLE Avails
ADD PRIMARY KEY(Loan_Number, Customer_id);

            -- Adding FOREIGN KEYS
ALTER TABLE Avails
ADD CONSTRAINT customer_avails1 FOREIGN KEY(Loan_Number)
REFERENCES Loan(Loan_Number) ON DELETE SET NULL;

ALTER TABLE Avails
ADD CONSTRAINT customer_avails2 FOREIGN KEY(Customer_id)
REFERENCES Customer(Customer_id) ON DELETE SET NULL;


    -- A Customer can have multiple phone numbers(multivalued attribute)
ALTER TABLE Customer_Phone
ADD CONSTRAINT Customer_has FOREIGN KEY(Customer_id)
REFERENCES Customer(Customer_id) ON DELETE SET NULL;

ALTER TABLE Customer_Phone
ADD PRIMARY KEY(Customer_id, customer_phone);



    -- Add FOREIGN KEYS and COMPOSITE PRIMARY  KEY TO Related Dependent
ALTER TABLE Related_Dependent
ADD Related_Customer_Id number(10);

ALTER TABLE Related_Dependent
ADD CONSTRAINT customer_related_to FOREIGN KEY(Related_Customer_Id)
REFERENCES Customer(Customer_id) ON DELETE SET NULL;

ALTER TABLE Related_Dependent
ADD PRIMARY KEY(Related_Customer_Id, Name);




